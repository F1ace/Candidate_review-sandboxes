def _strip_think(content: Optional[str]) -> str:
    if not content:
        return ""
    if "</think>" in content:
        return content.split("</think>", 1)[1].strip()
    return content.replace("<think>", "").strip()

def _strip_intro(text: str, intro_done: bool) -> str:
    """Cut repetitive greetings when intro already done."""
    if not intro_done or not text:
        return text
    intro_patterns = [
        "РїСЂРёРІРµС‚", "РґРѕР±СЂС‹Р№ РґРµРЅСЊ", "Р·РґСЂР°РІСЃС‚РІСѓР№С‚Рµ", "СЏ РїСЂРѕРІРµРґСѓ СЃРѕР±РµСЃРµРґРѕРІР°РЅРёРµ", "С„РѕСЂРјР°С‚ СЃРѕСЃС‚РѕРёС‚",
        "СЃРµРіРѕРґРЅСЏ РјС‹ РїСЂРѕР№РґС‘Рј", "РјС‹ РїСЂРѕРІРµРґС‘Рј", "РґР°РІР°Р№С‚Рµ РїСЂРёСЃС‚СѓРїРёРј", "РЅР°С‡РЅС‘Рј СЃ С‚РµРѕСЂРёРё",
    ]
    lowered = text.lower()
    for pat in intro_patterns:
        if lowered.startswith(pat):
            # Remove first sentence
            parts = text.split("\n", 1)
            return parts[1] if len(parts) > 1 else ""
    return text

