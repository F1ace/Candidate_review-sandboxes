def _strip_think(content: Optional[str]) -> str:
    if not content:
        return ""
    if "</think>" in content:
        return content.split("</think>", 1)[1].strip()
    return content.replace("<think>", "").strip()


def _strip_intro(text: str, intro_done: bool) -> str:
    """Cut repetitive greetings when intro already happened."""
    if not intro_done or not text:
        return text

    intro_patterns = [
        "РїСЂРёРІРµС‚", "РґРѕР±СЂС‹Р№ РґРµРЅСЊ", "Р·РґСЂР°РІСЃС‚РІСѓР№С‚Рµ", "СЏ РїСЂРѕРІРµРґСѓ СЃРѕР±РµСЃРµРґРѕРІР°РЅРёРµ", "С„РѕСЂРјР°С‚ СЃРѕСЃС‚РѕРёС‚",
        "СЃРµРіРѕРґРЅСЏ РјС‹ РїСЂРѕР№РґС‘Рј", "РјС‹ РїСЂРѕРІРµРґС‘Рј", "РґР°РІР°Р№С‚Рµ РїСЂРёСЃС‚СѓРїРёРј", "РЅР°С‡РЅС‘Рј СЃ С‚РµРѕСЂРёРё",
    ]
    lowered = text.lower()
    if any(lowered.startswith(pattern) for pattern in intro_patterns):
        parts = text.split("\n", 1)
        return parts[1] if len(parts) > 1 else ""
    return text

def _extract_inline_tool_call(content: str) -> tuple[str, dict[str, Any]] | None:
    """
    Fallback: РЅРµРєРѕС‚РѕСЂС‹Рµ РјРѕРґРµР»Рё РїРµС‡Р°С‚Р°СЋС‚ tool-call С‚РµРєСЃС‚РѕРј, РЅР°РїСЂРёРјРµСЂ:
    <|start|>assistant<channel>commentary to=score_task <|constrain|>json<message|>{...}
    РёР»Рё:
    <|start|>assistant<channel>commentary to=functions.score_task<constrain>json<message>{...}

    Р’РѕР·РІСЂР°С‰Р°РµС‚ (tool_name, args) РёР»Рё None.
    """
    if not content:
        return None

    # 1) Р’С‹С‚Р°СЃРєРёРІР°РµРј РёРјСЏ "to=...". РџРѕРґРґРµСЂР¶РёРІР°РµРј С‚РѕС‡РєСѓ: functions.score_task
    m = re.search(r"to=([a-zA-Z_][a-zA-Z0-9_]*(?:\.[a-zA-Z_][a-zA-Z0-9_]*)?)", content)
    ALLOWED_INLINE_TOOLS = {"rag_search", "run_code", "run_sql", "score_task"}
    if not m:
        return None

    raw_name = m.group(1)
    # РµСЃР»Рё РїСЂРёС€Р»Рѕ functions.score_task вЂ” Р±РµСЂС‘Рј РїРѕСЃР»РµРґРЅСЋСЋ С‡Р°СЃС‚СЊ
    tool_name = raw_name.split(".")[-1].strip()

    if allowed_tools is None:
        allowed_tools = set()
    if tool_name not in allowed_tools:
        return None

    # 2) Р’С‹С‚Р°СЃРєРёРІР°РµРј JSON РјРµР¶РґСѓ { ... }
    start = content.find("{")
    end = content.rfind("}")
    if start == -1 or end == -1 or end <= start:
        return None

    raw_json = content[start : end + 1]
    try:
        payload = json.loads(raw_json)
    except Exception:
        return None

    if not isinstance(payload, dict):
        return None

    return tool_name, payload


